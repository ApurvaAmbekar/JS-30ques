// String [a sequence of characters used to represent text]
let str ="Apurva";
console.log(str);
console.log(typeof(str));


// Boolean can only have two values: true or false.
let x = 3;
let y = 5;
console.log(x==y);
console.log(typeof((x==y)));

//undefined [ a variable without a value, has the value undefined OR If a variable is declared but the value is not assigned, then the value of that variable will be undefined]
let Ash;
Ash = undefined;
console.log(Ash);
console.log(typeof(Ash));


// null [is a special value that represents an empty or unknown value]
let number = null;
console.log(number);
console.log(typeof(number));

